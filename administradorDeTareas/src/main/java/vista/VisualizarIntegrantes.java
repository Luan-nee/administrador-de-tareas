  public void refrescarListaIntegrantes(){
    DefaultTableModel modelo = new DefaultTableModel(){
        @Override
        public boolean isCellEditable(int row,int colum){
            return false;
        }
    };

    modelo.addColumn("ID");
    modelo.addColumn("NOMBRE DEL INTEGRANTE");
    modelo.addColumn("EMAIL");

    List<String[]> datos = this.obtenerListaIntegrantes();
    for(String[] dato : datos){
        modelo.addRow(dato);
    }

    this.tabla_integrantesDelProyecto.setModel(modelo);
  }
  
