  private void btn_guardarCambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarCambiosActionPerformed
    if("".equals(txt_nombreTarea.getText()) || "".equals(txt_descTareaAsignada.getText()) || "".equals(txt_idResponsable.getText())){      
      JOptionPane.showMessageDialog(null,"NO SE PERMITEN CAMPOS VACIOS");
    }else{
      int id_proyecto = Integer.parseInt(txt_idTareaAsignada.getText());
      String nombre = txt_nombreTarea.getText();
      String desc = txt_descTareaAsignada.getText();
      String cbx_prio = (String) cbx_prioridadTarea.getSelectedItem();
      String fechalimite = txt_fechaLimite.getText();
      int id_responsable = Integer.parseInt(txt_idResponsable.getText());
      
      this.ventanaGTCA.controllerGTCA.guardarCambiosDeTareaAsignada(id_proyecto, nombre, desc, cbx_prio, fechalimite, id_responsable);
      System.out.println("LA TAREA ASIGNADA SE MODIFICO");
      
      this.ventanaGTCA.refrescarTablaTareasAsignadas();
    }
  }//GEN-LAST:event_btn_guardarCambiosActionPerformed

