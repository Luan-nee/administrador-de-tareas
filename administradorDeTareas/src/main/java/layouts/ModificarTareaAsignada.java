  private void btn_buscarTareaSignadaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarTareaSignadaActionPerformed
    if("".equals(txt_idTareaAsignada.getText())){
      JOptionPane.showMessageDialog(null,"INGRESE EL ID DE LA TAREA ASIGNADA");
    }else{
      List<String[]> datos = ventanaGTCA.controllerGTCA.obtenerDatosDeUnaTareaAsignada(Integer.parseInt(txt_idTareaAsignada.getText()),ventanaGTCA.id_proyecto);
      txt_nombreTarea.setText(datos.get(0)[0]);
      txt_descTareaAsignada.setText(datos.get(0)[1]);
      cbx_prioridadTarea.setSelectedItem(datos.get(0)[2]);
      txt_fechaLimite.setText(datos.get(0)[3]);
      txt_idResponsable.setText(datos.get(0)[4]);
    }
  }//GEN-LAST:event_btn_buscarTareaSignadaActionPerformed

