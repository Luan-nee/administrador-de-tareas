  private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    String nombre = txt_nombre.getText();
    String desc = txt_desc.getText();
    String prio = (String) cbx_prioridad.getSelectedItem();
    String fechaLimite = txt_fechaLimite.getText();
    int id_responsable = Integer.parseInt(txt_responsable.getText());
    
    this.ventanaGTCA.controllerGTCA.guardarTarea(this.ventanaGTCA.id_proyecto, nombre, desc, prio, fechaLimite, id_responsable, this.ventanaGTCA.id_user);
    System.out.println("LA TAREA FUE ASIGNADA EXITOSAMENTE");
    ventanaGTCA.refrescarTablaTareasAsignadas();
  }//GEN-LAST:event_jButton2ActionPerformed

  private void txt_fechaLimiteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txt_fechaLimiteMouseClicked
    this.calendarioANT = new CalendarioAsignarNuevaTarea(this.ventanaGTCA, true);
    this.calendarioANT.obtenerCalendario().setMinSelectableDate(new Date());
    this.calendarioANT.obtenerVentanaAnterior(this);
    this.calendarioANT.setVisible(true);
  }//GEN-LAST:event_txt_fechaLimiteMouseClicked

