    private void Btn_eliminarIntegranteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_eliminarIntegranteActionPerformed
        if("".equals(txtEmail.getText())){
          JOptionPane.showMessageDialog(null,"CAMPO SIN RELLENAR");
        }else{
          this.MenuAdministrador.controllerGTCA.eliminarIntegrante(Integer.parseInt(txtEmail.getText()), MenuAdministrador.id_proyecto);
          this.vAnterior.refrescarListaIntegrantes();
          System.out.println("EL INTEGRANTE FUE RETIRADO DEL PROYECTO");
        }
    }//GEN-LAST:event_Btn_eliminarIntegranteActionPerformed

